#!/usr/bin/env python3

"""
sum_list function that takes a list input_list of floats as argument and returns their sum as a float.
"""

from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    sum_list function that takes a list input_list of floats as argument and returns their sum as a float.
    """
    return sum(input_list)
